                <h1 class="title">NParks looking into ways to reduce impact of beachgoers on marine creatures</h1>
                <img class="bigPicture center-block" src="https://static1.straitstimes.com.sg/s3fs-public/styles/large30x20/public/articles/2022/02/05/af_changibeachanimals_050222.jpg?VersionId=YhT86nKvJM0aTnfTDiProxuSGWduX_YJ&itok=BiS9hTej"/>
                <p class="text" style="margin-left: 10em; margin-right: 10em">On Feb 1, families flocked to Changi Beach during low tide to harvest sea creatures. PHOTOS: THADDEUS ANG, DESMOND LEE/FACEBOOK</p>
            </div>
            
            <div class="row" style="padding-right: 8em; padding-left: 8em">
                <p class="center-block text">Written by Shermaine Ang, PUBLISHED FEB 5, 2022, 12:18 PM SGT</p>
                <hr>
                
                <p class="details text">SINGAPORE - Beachgoers collecting sea creatures with buckets and tongs at low tide are a common sight in Singapore.</p>
                <p class="details text">The authorities are now looking into how the impact of such behaviour on the creatures can be reduced, following a recent spate of cases.</p>
                <p class="details text">On Tuesday (Feb 1), families flocked to Changi Beach during low tide to harvest sea creatures. A video by local science and nature education channel Just Keep Thinking shows people digging up sea creatures like sea urchins.</p>
                <p class="details text">After volunteers explained to them that the animals require seawater to survive, some families released them back into the wild.</p>
                <p class="details text">In a Facebook post on Friday, Minister for National Development Desmond Lee reminded people not to remove marine creatures from the intertidal zone, as they will not survive outside their natural marine environment.</p>
                <p class="details text">Extracting them affects their population and threatens the existing balance, he added.</p>
                <p class="details text">He said the National Parks Board (NParks) is looking into ways to curb the impact visitors have on the intertidal zone, and will discuss solutions for better visitor management with stakeholders, including nature guides and nature groups.</p>
                <p class="details text">NParks will put up more educational signs on the dos and don'ts when visiting the beaches, Mr Lee added.</p>
                <p class="details text">The board said in June last year that it would take extra steps to prevent beachgoers from manhandling marine creatures in intertidal areas, following an influx of people to Changi Beach during low tide.</p>
                <p class="details text">These include stepping up patrols by NParks staff, volunteers and nature groups.</p>
                <p class="details text">NParks said on Saturday that it was aware of the large number of visitors at Changi Beach over the Chinese New Year holiday. </p>
                <p class="details text">It sent volunteers to conduct outreach efforts at Changi Beach, East Coast Park and St John’s Island on Wednesday. </p>
                <p class="details text">It added that visitors should refrain from touching, collecting or trampling on any wildlife, as some are sensitive to tactile disturbances.</p>
                <p class="details text">They should also beware of stinging or venomous marine life, and wear covered shoes to protect against sharp rocks or creatures with spines in the intertidal zones.</p>
                <p class="details text">NParks said it will be putting up more educational signs on the proper etiquette when visiting beaches. </p>
                <p class="details text">Ms Kong Man Jing, 27, co-founder of Just Keep Thinking, said many of the families she saw at Changi Beach had young children and were collecting bucketfuls of sea creatures to entertain the children.</p>
                <p class="details text">They took the animals home to keep as pets. </p>
                <p class="details text">While some families were receptive and returned the animals to the sea, others turned aggressive when they were approached.</p>
                <p class="details text">“It was frustrating. They don’t see the animals as living things, and they’re teaching their kids that it’s okay if the animals die, they can just be thrown away,” Ms Kong said.</p>
                <p class="details text">But she tries to reach a compromise with the most resistant visitors, and thinks it is worth the effort. </p>
                <p class="details text">“Every family who has a change in mindset is worth celebrating; it can go a long way, as they can influence others,” she said, adding that she has the support of fellow nature educators and a growing number of volunteers. </p>
                <p class="details text">Ms Kong, who specialised in marine biology in university and volunteers at the beach around twice a week, added: “Some people think it’s a trivial matter, and ask why should we care so much?</p>
                <p class="details text">“But all you have to do is to be there and see for yourself how these people are treating the animals - and the people trying to explain the harms - to know it’s not a small issue.</p>
                <p class="details text">“It’s our animals and our shore, so we do have a reason to protect it.”</p>
